import React, { useState } from 'react';
import { Link, useNavigate } from 'react-router-dom';
import { auth } from '../firebase.ts';
import { GoogleAuthProvider, FacebookAuthProvider, signInWithPopup } from 'firebase/auth';
import Navbar from './Navbar';
import './LoginPage.css';
import Footer from "./Footer";
import Notification from './Notification';
import { apiRequest, API_CONFIG } from '../utils/apiConfig';
import { jwtDecode } from 'jwt-decode'; // Th√™m import n√†y

interface LoginRequest {
  email: string;
  password: string;
}

interface LoginResponse {
  success: boolean;
  message: string;
  accessToken?: string;
  refreshToken?: string;
  user?: {
    id: string;
    email: string;
    name: string;
    role: string;
  };
}

// Th√™m interface cho JWT payload
interface JWTPayload {
  sub?: string;
  email?: string;
  role?: string;
  name?: string;
  exp?: number;
  iat?: number;
  jti?: string;
  // Microsoft Identity claims
  'http://schemas.xmlsoap.org/ws/2005/05/identity/claims/nameidentifier'?: string;
  'http://schemas.xmlsoap.org/ws/2005/05/identity/claims/emailaddress'?: string;
  'http://schemas.microsoft.com/ws/2008/06/identity/claims/role'?: string;
  [key: string]: any;
}


const LoginPage: React.FC = () => {
  const [email, setEmail] = useState('');
  const [password, setPassword] = useState('');
  const [isLoading, setIsLoading] = useState(false);
  const [notification, setNotification] = useState({
    isVisible: false,
    message: '',
    type: 'success' as 'success' | 'error'
  });

  const navigate = useNavigate();

  const showNotification = (message: string, type: 'success' | 'error') => {
    setNotification({
      isVisible: true,
      message,
      type
    });
  };

  const hideNotification = () => {
    setNotification(prev => ({ ...prev, isVisible: false }));
  };

  const saveTokens = (accessToken: string, refreshToken: string) => {
    localStorage.setItem('accessToken', accessToken);
    localStorage.setItem('refreshToken', refreshToken);
  };

  const saveUser = (user: any) => {
    localStorage.setItem('userInfo', JSON.stringify(user));
  };

  // Th√™m function ƒë·ªÉ decode JWT token
  const decodeToken = (token: string): JWTPayload | null => {
    try {
      const decoded = jwtDecode<JWTPayload>(token);

      // Extract email from Microsoft claims
      const email = decoded['http://schemas.xmlsoap.org/ws/2005/05/identity/claims/emailaddress'] ||
          decoded.email;

      // Extract role from Microsoft claims
      const role = decoded['http://schemas.microsoft.com/ws/2008/06/identity/claims/role'] ||
          decoded.role;

      return {
        ...decoded,
        email: email,
        role: role
      };
    } catch (error) {
      return null;
    }
  };


  // Th√™m function ƒë·ªÉ ki·ªÉm tra admin
  const isAdminUser = (user: any, token?: string): boolean => {

    // Ki·ªÉm tra t·ª´ user object tr∆∞·ªõc
    if (user?.role === 'admin' || user?.email === 'admin@cuoidi.dev') {
      return true;
    }

    // N·∫øu c√≥ token, decode ƒë·ªÉ ki·ªÉm tra
    if (token) {
      const decodedToken = decodeToken(token);
      if (decodedToken) {
        // L·∫•y email t·ª´ Microsoft claims
        const tokenEmail = decodedToken['http://schemas.xmlsoap.org/ws/2005/05/identity/claims/emailaddress'] ||
            decodedToken.email;

        // L·∫•y role t·ª´ Microsoft claims (c√≥ th·ªÉ l√† GUID c·ªßa role admin)
        const tokenRole = decodedToken['http://schemas.microsoft.com/ws/2008/06/identity/claims/role'] ||
            decodedToken.role;

        // Ki·ªÉm tra email admin
        const isAdminEmail = tokenEmail === 'admin@cuoidi.dev';

        // Ki·ªÉm tra role (c√≥ th·ªÉ c·∫ßn th√™m logic ƒë·ªÉ map GUID role th√†nh text)
        const isAdminRole = tokenRole === 'admin' ||
            tokenRole === 'ADMIN' ||
            // N·∫øu b·∫°n bi·∫øt GUID c·ªßa admin role, th√™m v√†o ƒë√¢y
            tokenRole === '960a814f-467c-4303-a951-3db74ea30aef'; // V√≠ d·ª• t·ª´ token c·ªßa b·∫°n


        if (isAdminEmail || isAdminRole) {
          return true;
        }
      }
    }
    return false;
  };


  const loginAPI = async (credentials: LoginRequest): Promise<LoginResponse> => {
    try {
      const response = await apiRequest(API_CONFIG.ENDPOINTS.AUTH.LOGIN, {
        method: 'POST',
        body: JSON.stringify(credentials),
      });

      const data = await response.json();

      if (response.ok) {
        // Handle different possible API response structures
        let accessToken, refreshToken, user;

        if (data.result) {
          // If API uses result wrapper
          accessToken = data.result.accessToken || data.result.access_token || data.result.token;
          refreshToken = data.result.refreshToken || data.result.refresh_token;
          user = data.result.user || data.result.userInfo || data.result;
        } else {
          // Direct response structure
          accessToken = data.accessToken || data.access_token || data.token;
          refreshToken = data.refreshToken || data.refresh_token;
          user = data.user || data.userInfo || data;
        }

        return {
          success: true,
          message: data.message || 'ƒêƒÉng nh·∫≠p th√†nh c√¥ng!',
          accessToken,
          refreshToken,
          user,
        };
      } else {
        return {
          success: false,
          message: data.message || 'ƒêƒÉng nh·∫≠p th·∫•t b·∫°i. Vui l√≤ng ki·ªÉm tra l·∫°i th√¥ng tin.',
        };
      }
    } catch (error) {
      console.error('Login error:', error);
      return {
        success: false,
        message: 'C√≥ l·ªói x·∫£y ra. Vui l√≤ng ki·ªÉm tra k·∫øt n·ªëi m·∫°ng v√† th·ª≠ l·∫°i.',
      };
    }
  };

  const getDeviceId = () => {
    // Simple deviceId generator (customize as needed)
    let deviceId = localStorage.getItem('deviceId');
    if (!deviceId) {
      deviceId = Math.random().toString(36).substring(2) + Date.now();
      localStorage.setItem('deviceId', deviceId);
    }
    return deviceId;
  };

  const handleSocialLogin = async (providerType: 'google' | 'facebook') => {
    setIsLoading(true);
    try {
      const provider =
          providerType === 'google'
              ? new GoogleAuthProvider()
              : new FacebookAuthProvider();

      const result = await signInWithPopup(auth, provider);
      const idToken = await result.user.getIdToken();
      const deviceId = getDeviceId();

      // Send to your API
      const response = await apiRequest(API_CONFIG.ENDPOINTS.AUTH.FIREBASE_LOGIN, {
        method: 'POST',
        body: JSON.stringify({ idToken, deviceId }),
      });
      const data = await response.json();

      if (response.ok && (data.accessToken || data.result?.accessToken)) {
        // Save tokens and user info as before
        const accessToken = data.accessToken || data.result?.accessToken;
        const refreshToken = data.refreshToken || data.result?.refreshToken;
        const user = data.user || data.result?.user;

        if (accessToken && refreshToken) saveTokens(accessToken, refreshToken);
        if (user) saveUser(user);

        showNotification('ƒêƒÉng nh·∫≠p th√†nh c√¥ng!', 'success');
        window.dispatchEvent(new Event('userInfoUpdated'));

        setTimeout(() => {
          // S·ª≠ d·ª•ng function isAdminUser m·ªõi v·ªõi token decode
          if (isAdminUser(user, accessToken)) {
            console.log('Navigating to admin page for social login user:', user);
            navigate('/admin');
          } else {
            console.log('Navigating to home page for social login user:', user);
            navigate('/home');
          }
        }, 1500);
      } else {
        showNotification(data.message || 'ƒêƒÉng nh·∫≠p th·∫•t b·∫°i.', 'error');
      }
    } catch (error) {
      console.error('Social login error:', error);
      showNotification('L·ªói ƒëƒÉng nh·∫≠p m·∫°ng x√£ h·ªôi.', 'error');
    } finally {
      setIsLoading(false);
    }
  };

  const handleSubmit = async (e: React.FormEvent) => {
    e.preventDefault();

    if (!email || !password) {
      showNotification('Vui l√≤ng nh·∫≠p ƒë·∫ßy ƒë·ªß email v√† m·∫≠t kh·∫©u', 'error');
      return;
    }

    setIsLoading(true);

    try {
      const credentials: LoginRequest = { email, password };
      const response = await loginAPI(credentials);

      if (response.success) {
        // Save tokens and user info
        if (response.accessToken && response.refreshToken) {
          saveTokens(response.accessToken, response.refreshToken);
        } else {
          console.log('No tokens in response:', {
            hasAccessToken: !!response.accessToken,
            hasRefreshToken: !!response.refreshToken
          });
        }

        if (response.user) {
          saveUser(response.user);
        }

        // Show success notification
        showNotification('ƒêƒÉng nh·∫≠p th√†nh c√¥ng!', 'success');

        // Dispatch custom event to notify other components
        window.dispatchEvent(new Event('userInfoUpdated'));

        // Navigate v·ªõi logic ki·ªÉm tra admin m·ªõi
        setTimeout(() => {
          // Decode token ƒë·ªÉ ki·ªÉm tra role
          console.log('Checking admin status for user:', response.user);
          console.log('Access token available:', !!response.accessToken);

          if (isAdminUser(response.user, response.accessToken)) {
            console.log('‚úÖ Admin user detected, navigating to /admin');
            navigate('/admin');
          } else {
            console.log('üë§ Regular user, navigating to /home');
            navigate('/home');
          }
        }, 1500);
      } else {
        console.log('Login failed:', response.message);
        // Show error notification
        showNotification(response.message || 'ƒêƒÉng nh·∫≠p th·∫•t b·∫°i. Vui l√≤ng ki·ªÉm tra l·∫°i th√¥ng tin.', 'error');
      }
    } catch (error) {
      console.error('Login error:', error);
      showNotification('L·ªói k·∫øt n·ªëi. Vui l√≤ng th·ª≠ l·∫°i sau.', 'error');
    } finally {
      setIsLoading(false);
    }
  };

  return (
    <div className="login-page">
      <Notification
        message={notification.message}
        type={notification.type}
        isVisible={notification.isVisible}
        onClose={hideNotification}
      />

      <Navbar />
      <div className="login-container">
        <div className="login-form-container">
          <h1>ƒêƒÉng Nh·∫≠p</h1>
          <p className="login-subtitle">ƒêƒÉng nh·∫≠p ƒë·ªÉ truy c·∫≠p t√†i kho·∫£n c·ªßa b·∫°n</p>

          <form className="login-form" onSubmit={handleSubmit}>
            <div className="form-group">
              <label htmlFor="email">Email</label>
              <input
                type="email"
                id="email"
                value={email}
                onChange={(e) => setEmail(e.target.value)}
                required
                placeholder="Nh·∫≠p ƒë·ªãa ch·ªâ email c·ªßa b·∫°n"
                disabled={isLoading}
              />
            </div>

            <div className="form-group">
              <label htmlFor="password">M·∫≠t kh·∫©u</label>
              <input
                type="password"
                id="password"
                value={password}
                onChange={(e) => setPassword(e.target.value)}
                required
                placeholder="Nh·∫≠p m·∫≠t kh·∫©u c·ªßa b·∫°n"
                disabled={isLoading}
              />
            </div>

            <div className="form-options">
              <div className="remember-me">
                <input type="checkbox" id="remember" disabled={isLoading} />
                <label htmlFor="remember">Ghi nh·ªõ ƒëƒÉng nh·∫≠p</label>
              </div>
              <Link to="/forgot-password" className="forgot-password">Qu√™n m·∫≠t kh·∫©u?</Link>
            </div>

            <button type="submit" className="login-button" disabled={isLoading}>
              {isLoading ? 'ƒêang ƒëƒÉng nh·∫≠p...' : 'ƒêƒÉng Nh·∫≠p'}
            </button>

            {/* Divider with "ho·∫∑c" text */}
            <div className="social-divider">
              <div className="divider-line"></div>
              <span className="divider-text">ho·∫∑c</span>
              <div className="divider-line"></div>
            </div>

            {/* Social login buttons */}
            <div className="social-login-buttons">
              <button
                  type="button"
                  className="social-button google-button"
                  disabled={isLoading}
                  onClick={() => handleSocialLogin('google')}
              >
                <img src="/icons8-google-240.svg" alt="Google" />
                ƒêƒÉng nh·∫≠p v·ªõi Google
              </button>
              <button
                  type="button"
                  className="social-button facebook-button"
                  disabled={isLoading}
                  onClick={() => handleSocialLogin('facebook')}
              >
                <img src="/icons8-facebook-144.svg" alt="Facebook" />
                ƒêƒÉng nh·∫≠p v·ªõi Facebook
              </button>
            </div>
          </form>

          <div className="login-footer">
            <p>Ch∆∞a c√≥ t√†i kho·∫£n? <Link to="/register">ƒêƒÉng k√Ω ngay</Link></p>
          </div>
        </div>

        <div className="login-image">
          <div className="login-image-content">
            <h2>Ch√†o m·ª´ng ƒë·∫øn v·ªõi Cuoidi.vn</h2>
            <p>N·ªÅn t·∫£ng k·∫øt n·ªëi d·ªãch v·ª• c∆∞·ªõi h·ªèi h√†ng ƒë·∫ßu Vi·ªát Nam</p>
          </div>
        </div>
      </div>
      <Footer />
    </div>
  );
};

export default LoginPage;
